function initials = getInitials(mouseNum)

if mouseNum<=18
    initials = 'LT';
elseif mouseNum<=26
    initials = 'DA';
elseif mouseNum<=46
    initials = 'VS';
elseif mouseNum<=58
    initials = 'TL';
elseif mouseNum<=68
    initials = 'JD';
elseif mouseNum<=78
    initials = 'AN';
else
    initials = 'SK';
end